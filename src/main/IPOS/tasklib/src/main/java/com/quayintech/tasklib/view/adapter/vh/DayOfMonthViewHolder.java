package com.quayintech.tasklib.view.adapter.vh;

import android.support.v7.widget.RecyclerView;

import com.quayintech.tasklib.view.SquareDayView;

/**
 * Created by deepak.kumar1 on 04-05-2018.
 */

public class DayOfMonthViewHolder extends RecyclerView.ViewHolder {
    final SquareDayView view;

    public DayOfMonthViewHolder(SquareDayView dayOfMonthView) {
        super(dayOfMonthView);
        this.view = dayOfMonthView;
    }

    public void setData(int dayOfMonth, boolean isChecked) {
        this.view.setDayOfMonth(dayOfMonth);
        this.view.setChecked(isChecked);
    }
}
