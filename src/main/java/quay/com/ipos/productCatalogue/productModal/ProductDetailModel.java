package quay.com.ipos.productCatalogue.productModal;

import java.util.ArrayList;

/**
 * Created by niraj.kumar on 5/27/2018.
 */

public class ProductDetailModel {
    private boolean isImage;
    private boolean isVideo;
    private String imageURL;
    private String video;
    private String videoPreviewImage;
    private String productTitle;
    private String productIcon;
    private String companyName;
    private String productId;
    private String storeId;

    private ArrayList<ProductChild> productDescription;


    public String getCompanyName() {
        return companyName;
    }

    public void setCompanyName(String companyName) {
        this.companyName = companyName;
    }

    public String getProductId() {
        return productId;
    }

    public void setProductId(String productId) {
        this.productId = productId;
    }

    public String getStoreId() {
        return storeId;
    }

    public void setStoreId(String storeId) {
        this.storeId = storeId;
    }



    public boolean isImage() {
        return isImage;
    }

    public void setImage(boolean image) {
        isImage = image;
    }

    public boolean isVideo() {
        return isVideo;
    }

    public void setVideo(boolean video) {
        isVideo = video;
    }

    public String getImageURL() {
        return imageURL;
    }

    public void setImageURL(String imageURL) {
        this.imageURL = imageURL;
    }

    public String getVideo() {
        return video;
    }

    public void setVideo(String video) {
        this.video = video;
    }

    public String getVideoPreviewImage() {
        return videoPreviewImage;
    }

    public void setVideoPreviewImage(String videoPreviewImage) {
        this.videoPreviewImage = videoPreviewImage;
    }


    public String getProductTitle() {
        return productTitle;
    }

    public void setProductTitle(String productTitle) {
        this.productTitle = productTitle;
    }

    public String getProductIcon() {
        return productIcon;
    }

    public void setProductIcon(String productIcon) {
        this.productIcon = productIcon;
    }


    public void setProductDescription(ArrayList<ProductChild> productDescription) {
        this.productDescription = productDescription;
    }


    public static class ProductChild{

        String heading;
        String description;
        boolean isCheckStock;

        public String getHeading() {
            return heading;
        }

        public void setHeading(String heading) {
            this.heading = heading;
        }

        public String getDescription() {
            return description;
        }

        public void setDescription(String description) {
            this.description = description;
        }

        public boolean isCheckStock() {
            return isCheckStock;
        }

        public void setCheckStock(boolean checkStock) {
            isCheckStock = checkStock;
        }
    }

}
